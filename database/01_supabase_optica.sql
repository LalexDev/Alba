CREATE OR REPLACE FUNCTION public.fn_crear_perfil_usuario()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_rol TEXT;
  v_id_rol BIGINT;
BEGIN
  v_rol := UPPER(COALESCE(NEW.raw_user_meta_data->>'rol', 'VENDEDOR'));
  IF v_rol NOT IN ('ADMINISTRADOR', 'VENDEDOR') THEN
    v_rol := 'VENDEDOR';
  END IF;

  SELECT id_rol INTO v_id_rol
  FROM public.roles
  WHERE nombre = v_rol;

  INSERT INTO public.usuarios (
    id_usuario, id_rol, nombres, apellidos, email, telefono
  ) VALUES (
    NEW.id,
    v_id_rol,
    COALESCE(NULLIF(NEW.raw_user_meta_data->>'nombres', ''), split_part(COALESCE(NEW.email, 'Usuario'), '@', 1)),
    COALESCE(NEW.raw_user_meta_data->>'apellidos', ''),
    COALESCE(NEW.email, NEW.id::TEXT || '@sin-correo.local'),
    NULLIF(NEW.raw_user_meta_data->>'telefono', '')
  )
  ON CONFLICT (id_usuario) DO UPDATE SET
    email = EXCLUDED.email,
    nombres = EXCLUDED.nombres,
    apellidos = EXCLUDED.apellidos,
    telefono = EXCLUDED.telefono,
    actualizado_en = NOW();

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_auth_usuario_creado ON auth.users;
CREATE TRIGGER trg_auth_usuario_creado
AFTER INSERT OR UPDATE OF email, raw_user_meta_data ON auth.users
FOR EACH ROW EXECUTE FUNCTION public.fn_crear_perfil_usuario();

CREATE OR REPLACE FUNCTION public.fn_usuario_activo()
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.usuarios u
    WHERE u.id_usuario = auth.uid()
      AND u.activo = TRUE
  );
$$;

CREATE OR REPLACE FUNCTION public.fn_es_admin()
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.usuarios u
    JOIN public.roles r ON r.id_rol = u.id_rol
    WHERE u.id_usuario = auth.uid()
      AND u.activo = TRUE
      AND r.nombre = 'ADMINISTRADOR'
  );
$$;

