CREATE OR REPLACE VIEW public.vw_productos_stock_bajo
WITH (security_invoker = true)
AS
SELECT
  p.id_producto,p.codigo_interno,p.codigo_barras,p.nombre,
  c.nombre AS categoria,m.nombre AS marca,p.stock_actual,p.stock_minimo,p.precio_venta
FROM public.productos p
JOIN public.categorias c ON c.id_categoria=p.id_categoria
LEFT JOIN public.marcas m ON m.id_marca=p.id_marca
WHERE p.activo=TRUE AND p.stock_actual<=p.stock_minimo;

CREATE OR REPLACE VIEW public.vw_ventas_diarias
WITH (security_invoker = true)
AS
SELECT
  DATE(v.fecha_venta) AS fecha,
  COUNT(*) FILTER (WHERE v.estado_venta='REGISTRADA') AS cantidad_ventas,
  COALESCE(SUM(v.total) FILTER (WHERE v.estado_venta='REGISTRADA'),0) AS total_vendido,
  COALESCE(SUM(v.a_cuenta) FILTER (WHERE v.estado_venta='REGISTRADA' AND v.metodo_pago='EFECTIVO'),0) AS total_efectivo,
  COALESCE(SUM(v.a_cuenta) FILTER (WHERE v.estado_venta='REGISTRADA' AND v.metodo_pago='YAPE'),0) AS total_yape,
  COALESCE(SUM(v.a_cuenta) FILTER (WHERE v.estado_venta='REGISTRADA' AND v.metodo_pago='TRANSFERENCIA'),0) AS total_transferencia
FROM public.ventas v
GROUP BY DATE(v.fecha_venta);

CREATE OR REPLACE VIEW public.vw_productos_mas_vendidos
WITH (security_invoker = true)
AS
SELECT
  p.id_producto,p.codigo_interno,p.nombre,
  COALESCE(SUM(dv.cantidad),0) AS unidades_vendidas,
  COALESCE(SUM(dv.subtotal),0) AS importe_vendido
FROM public.productos p
JOIN public.detalle_ventas dv ON dv.id_producto=p.id_producto
JOIN public.ventas v ON v.id_venta=dv.id_venta AND v.estado_venta='REGISTRADA'
GROUP BY p.id_producto,p.codigo_interno,p.nombre;

CREATE OR REPLACE VIEW public.vw_dashboard_resumen
WITH (security_invoker = true)
AS
SELECT
  COALESCE((SELECT SUM(total) FROM public.ventas WHERE estado_venta='REGISTRADA'),0) AS total_ventas,
  COALESCE((SELECT COUNT(*) FROM public.ventas WHERE estado_venta='REGISTRADA'),0) AS cantidad_ventas,
  COALESCE((SELECT COUNT(*) FROM public.productos WHERE activo=TRUE),0) AS total_productos,
  COALESCE((SELECT COUNT(*) FROM public.productos WHERE activo=TRUE AND stock_actual<=stock_minimo),0) AS bajo_stock,
  COALESCE((SELECT COUNT(*) FROM public.movimientos_inventario),0) AS movimientos_inventario;

