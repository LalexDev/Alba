CREATE OR REPLACE FUNCTION public.crear_producto(
  p_codigo_interno TEXT,
  p_codigo_barras TEXT,
  p_nombre TEXT,
  p_descripcion TEXT,
  p_modelo TEXT,
  p_color TEXT,
  p_medida TEXT,
  p_material TEXT,
  p_precio_compra NUMERIC,
  p_precio_venta NUMERIC,
  p_stock_inicial INTEGER,
  p_stock_minimo INTEGER,
  p_id_categoria BIGINT,
  p_id_marca BIGINT,
  p_id_proveedor BIGINT
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_producto public.productos%ROWTYPE;
  v_codigo_interno TEXT;
BEGIN
  IF NOT public.fn_es_admin() THEN
    RAISE EXCEPTION 'Solo el administrador puede crear productos';
  END IF;

  IF COALESCE(TRIM(p_codigo_barras), '') = '' OR COALESCE(TRIM(p_nombre), '') = '' THEN
    RAISE EXCEPTION 'Código de barras y nombre son obligatorios';
  END IF;

  v_codigo_interno := COALESCE(NULLIF(TRIM(p_codigo_interno), ''), TRIM(p_codigo_barras));

  INSERT INTO public.productos(
    id_categoria, id_marca, id_proveedor_preferido, codigo_interno, codigo_barras,
    nombre, descripcion, modelo, color, medida, material, precio_compra, precio_venta,
    stock_actual, stock_minimo
  ) VALUES (
    p_id_categoria, p_id_marca, p_id_proveedor, v_codigo_interno, TRIM(p_codigo_barras),
    TRIM(p_nombre), NULLIF(TRIM(p_descripcion), ''), NULLIF(TRIM(p_modelo), ''),
    NULLIF(TRIM(p_color), ''), NULLIF(TRIM(p_medida), ''), NULLIF(TRIM(p_material), ''),
    COALESCE(p_precio_compra, 0), COALESCE(p_precio_venta, 0),
    GREATEST(COALESCE(p_stock_inicial, 0), 0), GREATEST(COALESCE(p_stock_minimo, 0), 0)
  ) RETURNING * INTO v_producto;

  IF v_producto.stock_actual > 0 THEN
    INSERT INTO public.movimientos_inventario(
      id_producto, id_usuario, tipo_movimiento, cantidad, stock_anterior, stock_nuevo,
      referencia_tipo, referencia_id, motivo
    ) VALUES (
      v_producto.id_producto, auth.uid(), 'ENTRADA_INICIAL', v_producto.stock_actual,
      0, v_producto.stock_actual, 'PRODUCTO', v_producto.id_producto, 'Stock inicial del producto'
    );
  END IF;

  RETURN jsonb_build_object('id_producto', v_producto.id_producto);
END;
$$;

CREATE OR REPLACE FUNCTION public.registrar_cliente_receta(
  p_nombres TEXT,
  p_numero_documento TEXT,
  p_telefono TEXT,
  p_email TEXT,
  p_direccion TEXT,
  p_fecha_receta DATE,
  p_observaciones TEXT,
  p_lejos_od_esfera NUMERIC,
  p_lejos_od_cilindro NUMERIC,
  p_lejos_od_eje NUMERIC,
  p_lejos_oi_esfera NUMERIC,
  p_lejos_oi_cilindro NUMERIC,
  p_lejos_oi_eje NUMERIC,
  p_lejos_dip NUMERIC,
  p_cerca_od_esfera NUMERIC,
  p_cerca_od_cilindro NUMERIC,
  p_cerca_od_eje NUMERIC,
  p_cerca_oi_esfera NUMERIC,
  p_cerca_oi_cilindro NUMERIC,
  p_cerca_oi_eje NUMERIC,
  p_cerca_dip NUMERIC
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_id_cliente BIGINT;
  v_tiene_receta BOOLEAN;
BEGIN
  IF NOT public.fn_usuario_activo() THEN
    RAISE EXCEPTION 'Usuario no autorizado';
  END IF;
  IF COALESCE(TRIM(p_nombres), '') = '' THEN
    RAISE EXCEPTION 'El nombre del cliente es obligatorio';
  END IF;

  INSERT INTO public.clientes(
    tipo_persona, tipo_documento, numero_documento, nombres, telefono, email,
    direccion, observaciones, creado_por
  ) VALUES (
    'NATURAL',
    CASE WHEN COALESCE(TRIM(p_numero_documento), '') = '' THEN 'SIN_DOCUMENTO' ELSE 'DNI' END,
    NULLIF(TRIM(p_numero_documento), ''),
    TRIM(p_nombres), NULLIF(TRIM(p_telefono), ''), NULLIF(TRIM(p_email), ''),
    NULLIF(TRIM(p_direccion), ''), NULLIF(TRIM(p_observaciones), ''), auth.uid()
  ) RETURNING id_cliente INTO v_id_cliente;

  v_tiene_receta :=
    p_lejos_od_esfera IS NOT NULL OR p_lejos_od_cilindro IS NOT NULL OR p_lejos_od_eje IS NOT NULL OR
    p_lejos_oi_esfera IS NOT NULL OR p_lejos_oi_cilindro IS NOT NULL OR p_lejos_oi_eje IS NOT NULL OR
    p_lejos_dip IS NOT NULL OR p_cerca_od_esfera IS NOT NULL OR p_cerca_od_cilindro IS NOT NULL OR
    p_cerca_od_eje IS NOT NULL OR p_cerca_oi_esfera IS NOT NULL OR p_cerca_oi_cilindro IS NOT NULL OR
    p_cerca_oi_eje IS NOT NULL OR p_cerca_dip IS NOT NULL;

  IF v_tiene_receta THEN
    INSERT INTO public.recetas_opticas(
      id_cliente, fecha_receta,
      lejos_od_esfera, lejos_od_cilindro, lejos_od_eje,
      lejos_oi_esfera, lejos_oi_cilindro, lejos_oi_eje, lejos_dip,
      cerca_od_esfera, cerca_od_cilindro, cerca_od_eje,
      cerca_oi_esfera, cerca_oi_cilindro, cerca_oi_eje, cerca_dip,
      observaciones, creado_por
    ) VALUES (
      v_id_cliente, COALESCE(p_fecha_receta, CURRENT_DATE),
      p_lejos_od_esfera, p_lejos_od_cilindro, p_lejos_od_eje::SMALLINT,
      p_lejos_oi_esfera, p_lejos_oi_cilindro, p_lejos_oi_eje::SMALLINT, p_lejos_dip,
      p_cerca_od_esfera, p_cerca_od_cilindro, p_cerca_od_eje::SMALLINT,
      p_cerca_oi_esfera, p_cerca_oi_cilindro, p_cerca_oi_eje::SMALLINT, p_cerca_dip,
      NULLIF(TRIM(p_observaciones), ''), auth.uid()
    );
  END IF;

  RETURN jsonb_build_object('id_cliente', v_id_cliente);
END;
$$;

CREATE OR REPLACE FUNCTION public.ajustar_inventario(
  p_id_producto BIGINT,
  p_tipo_movimiento TEXT,
  p_cantidad INTEGER,
  p_motivo TEXT
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_producto public.productos%ROWTYPE;
  v_nuevo_stock INTEGER;
  v_es_entrada BOOLEAN;
BEGIN
  IF NOT public.fn_es_admin() THEN
    RAISE EXCEPTION 'Solo el administrador puede ajustar el inventario';
  END IF;
  IF p_cantidad IS NULL OR p_cantidad <= 0 THEN
    RAISE EXCEPTION 'La cantidad debe ser mayor que cero';
  END IF;
  IF p_tipo_movimiento NOT IN ('AJUSTE_ENTRADA','AJUSTE_SALIDA','DEVOLUCION_CLIENTE','DEVOLUCION_PROVEEDOR') THEN
    RAISE EXCEPTION 'Tipo de movimiento inválido';
  END IF;

  SELECT * INTO v_producto
  FROM public.productos
  WHERE id_producto = p_id_producto
  FOR UPDATE;

  IF NOT FOUND THEN RAISE EXCEPTION 'Producto no encontrado'; END IF;

  v_es_entrada := p_tipo_movimiento IN ('AJUSTE_ENTRADA','DEVOLUCION_CLIENTE');
  v_nuevo_stock := CASE WHEN v_es_entrada
    THEN v_producto.stock_actual + p_cantidad
    ELSE v_producto.stock_actual - p_cantidad
  END;

  IF v_nuevo_stock < 0 THEN
    RAISE EXCEPTION 'Stock insuficiente para realizar la salida';
  END IF;

  UPDATE public.productos
  SET stock_actual = v_nuevo_stock
  WHERE id_producto = p_id_producto;

  INSERT INTO public.movimientos_inventario(
    id_producto,id_usuario,tipo_movimiento,cantidad,stock_anterior,stock_nuevo,motivo
  ) VALUES (
    p_id_producto,auth.uid(),p_tipo_movimiento,p_cantidad,v_producto.stock_actual,v_nuevo_stock,NULLIF(TRIM(p_motivo),'')
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.registrar_venta(
  p_id_cliente BIGINT,
  p_metodo_pago TEXT,
  p_a_cuenta NUMERIC,
  p_observaciones TEXT,
  p_detalles JSONB
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_id_venta BIGINT;
  v_numero_venta TEXT;
  v_total NUMERIC(12,2) := 0;
  v_a_cuenta NUMERIC(12,2);
  v_saldo NUMERIC(12,2);
  v_estado_pago TEXT;
  v_item JSONB;
  v_producto public.productos%ROWTYPE;
  v_cantidad INTEGER;
  v_descuento NUMERIC(12,2);
  v_subtotal NUMERIC(12,2);
  v_id_caja BIGINT;
BEGIN
  IF NOT public.fn_usuario_activo() THEN
    RAISE EXCEPTION 'Usuario no autorizado';
  END IF;
  IF p_metodo_pago NOT IN ('EFECTIVO','YAPE','TRANSFERENCIA') THEN
    RAISE EXCEPTION 'Método de pago inválido';
  END IF;
  IF p_detalles IS NULL OR jsonb_typeof(p_detalles) <> 'array' OR jsonb_array_length(p_detalles) = 0 THEN
    RAISE EXCEPTION 'La venta debe contener productos';
  END IF;

  SELECT id_caja INTO v_id_caja
  FROM public.cajas
  WHERE id_usuario = auth.uid() AND estado = 'ABIERTA'
  ORDER BY fecha_apertura DESC
  LIMIT 1;

  INSERT INTO public.ventas(
    numero_venta,id_cliente,id_usuario,id_caja,subtotal,total,a_cuenta,saldo,
    metodo_pago,estado_pago,estado_venta,observaciones
  ) VALUES (
    'TEMP-' || gen_random_uuid()::TEXT,p_id_cliente,auth.uid(),v_id_caja,0,0,0,0,
    p_metodo_pago,'PENDIENTE','REGISTRADA',NULLIF(TRIM(p_observaciones),'')
  ) RETURNING id_venta INTO v_id_venta;

  v_numero_venta := 'OT-' || TO_CHAR(NOW(), 'YYYYMMDD') || '-' || LPAD(v_id_venta::TEXT, 6, '0');

  FOR v_item IN SELECT * FROM jsonb_array_elements(p_detalles)
  LOOP
    v_cantidad := COALESCE((v_item->>'cantidad')::INTEGER, 0);
    v_descuento := GREATEST(COALESCE((v_item->>'descuento')::NUMERIC, 0), 0);
    IF v_cantidad <= 0 THEN RAISE EXCEPTION 'Cantidad inválida'; END IF;

    SELECT * INTO v_producto
    FROM public.productos
    WHERE id_producto = (v_item->>'producto_id')::BIGINT
    FOR UPDATE;

    IF NOT FOUND THEN RAISE EXCEPTION 'Producto no encontrado'; END IF;
    IF NOT v_producto.activo THEN RAISE EXCEPTION 'El producto % está inactivo', v_producto.nombre; END IF;
    IF v_producto.stock_actual < v_cantidad THEN
      RAISE EXCEPTION 'Stock insuficiente para %: disponible %, solicitado %', v_producto.nombre, v_producto.stock_actual, v_cantidad;
    END IF;

    v_subtotal := GREATEST((v_producto.precio_venta * v_cantidad) - v_descuento, 0);

    INSERT INTO public.detalle_ventas(id_venta,id_producto,cantidad,precio_unitario,descuento,subtotal)
    VALUES (v_id_venta,v_producto.id_producto,v_cantidad,v_producto.precio_venta,v_descuento,v_subtotal);

    UPDATE public.productos
    SET stock_actual = stock_actual - v_cantidad
    WHERE id_producto = v_producto.id_producto;

    INSERT INTO public.movimientos_inventario(
      id_producto,id_usuario,tipo_movimiento,cantidad,stock_anterior,stock_nuevo,
      referencia_tipo,referencia_id,motivo
    ) VALUES (
      v_producto.id_producto,auth.uid(),'SALIDA_VENTA',v_cantidad,
      v_producto.stock_actual,v_producto.stock_actual-v_cantidad,
      'VENTA',v_id_venta,'Salida por venta ' || v_numero_venta
    );

    v_total := v_total + v_subtotal;
  END LOOP;

  v_a_cuenta := LEAST(GREATEST(COALESCE(p_a_cuenta, 0), 0), v_total);
  v_saldo := v_total - v_a_cuenta;
  v_estado_pago := CASE
    WHEN v_a_cuenta = 0 THEN 'PENDIENTE'
    WHEN v_saldo = 0 THEN 'PAGADO'
    ELSE 'PARCIAL'
  END;

  UPDATE public.ventas
  SET numero_venta = v_numero_venta,
      subtotal = v_total,
      total = v_total,
      a_cuenta = v_a_cuenta,
      saldo = v_saldo,
      estado_pago = v_estado_pago
  WHERE id_venta = v_id_venta;

  IF p_metodo_pago = 'EFECTIVO' AND v_a_cuenta > 0 AND v_id_caja IS NOT NULL THEN
    INSERT INTO public.movimientos_caja(id_caja,id_usuario,id_venta,tipo_movimiento,concepto,monto)
    VALUES (v_id_caja,auth.uid(),v_id_venta,'INGRESO','Cobro de ' || v_numero_venta,v_a_cuenta);
  END IF;

  INSERT INTO public.auditoria(id_usuario,accion,entidad,id_registro,datos_nuevos)
  VALUES (auth.uid(),'REGISTRAR_VENTA','ventas',v_id_venta,jsonb_build_object('numero',v_numero_venta,'total',v_total));

  RETURN jsonb_build_object(
    'id_venta',v_id_venta,
    'numero_venta',v_numero_venta,
    'total',v_total,
    'a_cuenta',v_a_cuenta,
    'saldo',v_saldo,
    'estado_pago',v_estado_pago
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.anular_venta(p_id_venta BIGINT, p_motivo TEXT)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_venta public.ventas%ROWTYPE;
  v_detalle RECORD;
  v_stock_anterior INTEGER;
BEGIN
  IF NOT public.fn_es_admin() THEN
    RAISE EXCEPTION 'Solo el administrador puede anular ventas';
  END IF;
  IF COALESCE(TRIM(p_motivo), '') = '' THEN
    RAISE EXCEPTION 'Debe indicar el motivo de anulación';
  END IF;

  SELECT * INTO v_venta FROM public.ventas WHERE id_venta = p_id_venta FOR UPDATE;
  IF NOT FOUND THEN RAISE EXCEPTION 'Venta no encontrada'; END IF;
  IF v_venta.estado_venta = 'ANULADA' THEN RAISE EXCEPTION 'La venta ya fue anulada'; END IF;

  FOR v_detalle IN SELECT * FROM public.detalle_ventas WHERE id_venta = p_id_venta
  LOOP
    SELECT stock_actual INTO v_stock_anterior
    FROM public.productos
    WHERE id_producto = v_detalle.id_producto
    FOR UPDATE;

    UPDATE public.productos
    SET stock_actual = stock_actual + v_detalle.cantidad
    WHERE id_producto = v_detalle.id_producto;

    INSERT INTO public.movimientos_inventario(
      id_producto,id_usuario,tipo_movimiento,cantidad,stock_anterior,stock_nuevo,
      referencia_tipo,referencia_id,motivo
    ) VALUES (
      v_detalle.id_producto,auth.uid(),'ANULACION_VENTA',v_detalle.cantidad,
      v_stock_anterior,v_stock_anterior+v_detalle.cantidad,'VENTA',p_id_venta,
      'Anulación de ' || v_venta.numero_venta
    );
  END LOOP;

  UPDATE public.ventas
  SET estado_venta='ANULADA',motivo_anulacion=TRIM(p_motivo),anulado_por=auth.uid(),anulado_en=NOW()
  WHERE id_venta=p_id_venta;

  INSERT INTO public.auditoria(id_usuario,accion,entidad,id_registro,datos_anteriores,datos_nuevos)
  VALUES (auth.uid(),'ANULAR_VENTA','ventas',p_id_venta,to_jsonb(v_venta),jsonb_build_object('motivo',TRIM(p_motivo)));
END;
$$;

