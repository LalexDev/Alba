-- TRIGGERS updated_at
-- =============================================================

DO $$
DECLARE
  tabla TEXT;
BEGIN
  FOREACH tabla IN ARRAY ARRAY[
    'usuarios','clientes','recetas_opticas','categorias','marcas','proveedores','productos',
    'compras','ventas','configuracion_empresa'
  ]
  LOOP
    EXECUTE format('DROP TRIGGER IF EXISTS %I ON public.%I', 'trg_' || tabla || '_actualizado', tabla);
    EXECUTE format(
      'CREATE TRIGGER %I BEFORE UPDATE ON public.%I FOR EACH ROW EXECUTE FUNCTION public.fn_actualizar_fecha_modificacion()',
      'trg_' || tabla || '_actualizado', tabla
    );
  END LOOP;
END;
$$;

